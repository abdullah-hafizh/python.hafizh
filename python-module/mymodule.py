import math

def area_of_square(side):
    area = side * side
    text = f'area of square with side {side} is {area}'
    print(text)

def area_of_triangle(height, base):
    area = 0.5 * height * base
    text = f'area of triangle with height {height} and base {base} is {area}'
    print(text)

def area_of_circle(r):
    area = 22/7 * r * r
    text = f'area of circle with radius r {r} is {area}'
    print(text)

